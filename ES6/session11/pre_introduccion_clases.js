/**
 * Pre-Introducción a las Clases
 * 
 * Las clases pueden tener propiedades, que son las caracteristicas que describen la clase
 * Ej: Carro tiene :
 * caracteristicas que son:
 * - puertas 
 * - color
 * - marca 
 * - modelo
 * - fabricante
 * - cc
 * métodos que son las funciones que realiza el objeto:
 * -apagar()
 * -acelerar()
 * -encender()
 * 
 * Herencia: Es que una clase le mande todas las propiedades a otra clase.
 * Ej Figura, Cuadrado y Rectangulo.
 * El objetovo es que la clase figura pueda mandarle las propiedades a las clases Cuadrado y Rectángulo.
 * Si a futuro se decide que las clases tengan alguna propiedad nueva, algun metodo nuevo, 
 * se crean en la clase Figura que es la clase padre, las demás son clases hijas.
 * suponiendo que los cuadrados y los rectangulos tengan métodos muy particulares
 * Figura => Cuadrado => ares: lado * lado
 * Figura => Rectángulo => ares: base * altura
 * 
 * En resumen:
 * CLASES son cosas que se pueden abstraer, cosas que pueden ayudar a manejar más facilmente los programas.
 * PROPIEDADES: cosas que describen las clases.
 * MÉTODOS son cosas que pueden hacer las clases.
 * HERENCIA: son las formas en las que los hijos pueden recibir propiedades del del padre.
 * 
 * No hay un límete de un nivel ereditario.
 */

